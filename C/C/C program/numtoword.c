#include<stdio.h>
void main(){
	int a;
	printf("Enter a Number::");
	scanf("%d",&a);
	
	
	if(a==0){
		printf("Zero");
	}
	if(a==1){
		printf("Un");
	}
	if(a==2){
		printf("Deux");
	}
	if(a==3){
		printf("Trois");
	}
	if(a==4){
		printf("Quatre");
	}
	if(a==5){
		printf("Cinq");
	}
	if(a==6){
		printf("Six");
	}
	if(a==7){
		printf("Sept");
	}
	if(a==8){
		printf("Huit");
	}
	if(a==9){
		printf("Neuf");
	}
}
